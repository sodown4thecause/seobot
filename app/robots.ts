import type { MetadataRoute } from 'next'

const baseUrl = process.env.NEXT_PUBLIC_SITE_URL || 'https://flowintent.com'

export default function robots(): MetadataRoute.Robots {
  return {
    rules: {
      userAgent: '*',
      allow: '/',
      disallow: [
        '/api/',
        '/admin/',
        '/dashboard/',
        '/onboarding/',
        '/login/',
        '/signup/',
        '/auth/',
        '/test-tailwind/',
      ],
    },
    sitemap: `${baseUrl}/sitemap.xml`,
  }
}

